#include "libft.h"

long	ft_atoi(const char *ptr)
{
	long	s;
	long	n;

	s = 1;
	n = 0;
	if (*ptr == '-')
	{
		s = s * -1;
		ptr++;
	}
	else if (*ptr == '+')
		ptr++;
	while (*ptr >= 48 && *ptr <= 57)
	{
		n = n * 10;
		n = n + ((int)*ptr - 48);
		ptr++;
	}
	return (n * s);
}
